const express = require("express");
const router = express.Router();

const { authenticate, authorize } = require("../middleware/authMiddleware");
const {
  createVenue,
  getAllVenues,
  getVenueById,
  updateVenue,
  deleteVenue,
  getNearbyVenues,
  deleteAllVenues,
  verifyVenue,
} = require("../controllers/venueController");

router.get("/nearby", getNearbyVenues);
// ğŸ“Œ Create Venue
router.post("/", authenticate, authorize(["admin", "customer"]), createVenue);

// ğŸ“Œ Verify Venue

router.patch("/:id/verify", authenticate, authorize(["admin"]), verifyVenue);
// ğŸ“Œ Get All Venues
router.get("/", authenticate, getAllVenues);

// ğŸ“Œ Get Venue by ID
router.get("/:id", authenticate, getVenueById);

// ğŸ“Œ Update Venue
router.put(
  "/:venueId",
  authenticate,
  authorize(["admin", "Customer"]),
  updateVenue
);

// ğŸ“Œ Delete one Venue
router.delete(
  "/:id",
  authenticate,
  authorize(["admin", "customer"]),
  deleteVenue
);
router.delete(
  "/",
  authenticate,
  authorize(["admin", "customer"]),
  deleteAllVenues
);

module.exports = router;
